#ifndef _OSH_NETDEFS_ETHERTYPES_H
#define _OSH_NETDEFS_ETHERTYPES_H

#ifndef ETH_P_IP
#define ETH_P_IP        0x0800  // IPv4
#endif

#ifndef ETH_P_ARP
#define ETH_P_ARP       0x0806 // ARP
#endif

#ifndef ETH_P_8021Q
#define ETH_P_8021Q     0x8100  // VLAN 802.1q
#endif

#ifndef ETH_P_IPV6
#define ETH_P_IPV6      0x86DD  // IPv6
#endif

#endif
